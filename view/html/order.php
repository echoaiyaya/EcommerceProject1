        (function detailBtn() {
            let detailBtn = document.querySelectorAll(".detailbtn");
            let orderDetail = document.querySelectorAll(".order-detail");
            detailBtn.forEach((e, k) => {
                detailBtn[k].onclick = () => {
                    if (detailBtn[k].getAttribute("open") == "false") {
                        orderDetail[k].style.display = "block";
                        detailBtn[k].setAttribute("open", "true");
                        detailBtn[k].innerHTML = "Hidden";
                    } else {
                        orderDetail[k].style.display = "none";
                        detailBtn[k].setAttribute("open", "false");
                        detailBtn[k].innerHTML = "Detail";
                    } 
                };
            });
        })();
